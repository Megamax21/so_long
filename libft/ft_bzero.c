#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*s_bis;

	s_bis = (char *)s;
	i = 0;
	while (n > 0)
	{
		s_bis[i] = '\0';
		n--;
		i++;
	}
}
